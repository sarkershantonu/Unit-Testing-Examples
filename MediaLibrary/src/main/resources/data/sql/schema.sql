CREATE TABLE IF NOT EXISTS t_Label ( ID int NOT NULL, NAME VARCHAR(256), PRIMARY KEY (ID))

CREATE TABLE IF NOT EXISTS t_Reviewer ( ID int NOT NULL, NAME VARCHAR(256), PRIMARY KEY (ID))

CREATE TABLE IF NOT EXISTS t_Genre ( ID int NOT NULL, NAME VARCHAR(256), PRIMARY KEY (ID))

CREATE TABLE IF NOT EXISTS t_Artist ( ID int NOT NULL, NAME VARCHAR(256), PRIMARY KEY (ID))

CREATE TABLE IF NOT EXISTS t_Review (  ID int NOT NULL,  REVIEWER_ID int,  RECORDING_ID int,  RATING int,  REVIEW VARCHAR(256),PRIMARY KEY (ID),  FOREIGN KEY (REVIEWER_ID))

CREATE TABLE IF NOT EXISTS t_Track(ID int NOT NULL,TITLE VARCHAR(256),DURATION VARCHAR(256),GENRE_ID int,ARTIST_ID int ,RECORDING_ID int,PRIMARY KEY (ID), FOREIGN KEY (GENRE_ID),  FOREIGN KEY (ARTIST_ID),   FOREIGN KEY (RECORDING_ID))

CREATE TABLE IF NOT EXISTS t_Recording( ID int NOT NULL, TITLE VARCHAR(256), RELEASE_DATE DATE, ART_ID int NOT NULL, LABEL_ID int NOT NULL, PRIMARY KEY (ID), FOREIGN KEY (ART_ID), FOREIGN KEY (LABEL_ID) )

